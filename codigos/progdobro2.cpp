#include <iostream>

/* Este programa serve para exemplificar o uso de variáveis mostrando o dobro de 10.*/

int main() {
	
	//Definindo o namespace:
	using namespace std;
	
	//Declarando a variável inteira com valor = 10:
	int x = 10;
	
	//Mostrando seu dobro:
	cout << "O valor de x é " << x << " e o dobro desse valor é " << x*2;
	
	return 0;
}
